    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Document doc = XMLJDomFunctions.lerDocumentoXML("ID_XMLFile.xml");
        String nome = jTextFieldEditNomeJogador.getText();
        String novoValor = jTextFieldEdit.getText();
        if (jDialogEdita.getTitle().equals("Edita Idade")) {
            int i = Integer.parseInt(novoValor);
            doc = ModeloXML.editaIdade(nome, i, doc);
        }
        if (jDialogEdita.getTitle().equals("Edita Nacionalidade")) {
            doc = ModeloXML.editaNacionalidade(nome, novoValor, doc);
        }
        if (jDialogEdita.getTitle().equals("Edita Clube Atual")) {
            doc = ModeloXML.editaClubeAtual(nome, novoValor, doc);
        }
        if (jDialogEdita.getTitle().equals("Edita Peso")) {
            double d = Double.parseDouble(novoValor);
            doc = ModeloXML.editaPeso(nome, d, doc);
        }
        if (jDialogEdita.getTitle().equals("Edita Pé Preferido")) {
            doc = ModeloXML.editaPrefPe(nome, novoValor, doc);
        }
        if (jDialogEdita.getTitle().equals("Edita Posição")) {
            doc = ModeloXML.editaPosicao(nome, novoValor, doc);
        }
        if (jDialogEdita.getTitle().equals("Edita Valor de Contrato")) {
            double d = Double.parseDouble(novoValor);
            doc = ModeloXML.editaVDC(nome, d, doc);
        }
        if (jDialogEdita.getTitle().equals("Edita Empresário")) {
            doc = ModeloXML.editaEmpresario(nome, novoValor, doc);
        }
        if (jDialogEdita.getTitle().equals("Edita Estado Atual")) {
            doc = ModeloXML.editaEstadoAtual(nome, novoValor, doc);
        }
        jDialogEdita.setVisible(false);
        if (doc != null) {
            XMLJDomFunctions.escreverDocumentoParaFicheiro(doc, "ID_XMLFile.xml");
            JOptionPane.showMessageDialog(this,
                    "Valor alterado com sucesso",
                    "Informação",
                    JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this,
                    "Jogador não existe",
                    "Informação",
                    JOptionPane.INFORMATION_MESSAGE);
        }
        jTextFieldEditNomeJogador.setText("");
        jTextFieldEdit.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

