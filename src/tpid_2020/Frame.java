    private void jButtonTransformaçãoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTransformaçãoActionPerformed
        // TODO add your handling code here:
        Element raiz = new Element("Valor");
        Document doc = new Document(raiz);
        raiz.addContent(jTextFieldTransformacao.getText());
        XMLJDomFunctions.escreverDocumentoParaFicheiro(doc, "valor.xml");
        if (jDialogTransformacoes.getTitle().equals("Transforma Por Clube")) {
            try {
                SaxonFunctions_XQuery.xQueryToXml("transformaClubes.xml", "transform2V2.xql");
                doc = XMLJDomFunctions.lerDocumentoXML("transformaClubes.xml");
                String t = XMLJDomFunctions.escreverDocumentoString(doc);
                OutputTextArea.setText(t);
            } catch (XPathException ex) {
                Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if (jDialogTransformacoes.getTitle().equals("Transforma Por Nacionalidade")) {
            try {
                SaxonFunctions_XQuery.xQueryToText("transformacaoNacionalidade.txt", "transform3V2.xql");
                Scanner ler = new Scanner(new FileInputStream("transformacaoNacionalidade.txt"));
                StringBuilder texto = new StringBuilder();
                String linha;
                while (ler.hasNextLine()) {
                    linha = ler.nextLine();
                    texto = texto.append(linha).append("\n");
                }
                ler.close();
                OutputTextArea.setText(texto.toString());
            } catch (XPathException ex) {
                Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if (jDialogTransformacoes.getTitle().equals("Transforma Por Empresario")) {
            try {
                // TODO add your handling code here:
                SaxonFunctions_XQuery.xQueryToXml("transformacaoEmpresario.xml", "transform5.xql");
                doc = XMLJDomFunctions.lerDocumentoXML("transformacaoEmpresario.xml");
                String t = XMLJDomFunctions.escreverDocumentoString(doc);
                OutputTextArea.setText(t);
            } catch (XPathException ex) {
                Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if (jDialogTransformacoes.getTitle().equals("Transforma Por Estado Atual")) {
            try {
                // TODO add your handling code here:
                SaxonFunctions_XQuery.xQueryToText("transformacaoEstadoAtual.txt", "transform6.xql");
                Scanner ler = new Scanner(new FileInputStream("transformacaoEstadoAtual.txt"));
                StringBuilder texto = new StringBuilder();
                String linha;
                while (ler.hasNextLine()) {
                    linha = ler.nextLine();
                    texto = texto.append(linha).append("\n");
                }
                ler.close();
                OutputTextArea.setText(texto.toString());
            } catch (XPathException ex) {
                Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        jDialogTransformacoes.setVisible(false);
        jTextFieldTransformacao.setText("");
    }//GEN-LAST:event_jButtonTransformaçãoActionPerformed

