    private void jMenuItemHTMLFotosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemHTMLFotosActionPerformed
        // TODO add your handling code here:
        Document doc = XMLJDomFunctions.lerDocumentoXML("ID_XMLFile.xml");
        if (doc != null) {
            Document novo = JDOMFunctions_XSLT.transformaDocumento(doc, "ID_XMLFile.xml", "transform1.xsl");
            XMLJDomFunctions.escreverDocumentoParaFicheiro(novo, "transformaImagens.html");
            doc = XMLJDomFunctions.lerDocumentoXML("transformaImagens.html");
            String t = XMLJDomFunctions.escreverDocumentoString(doc);
            OutputTextArea.setText(t);
            JOptionPane.showMessageDialog(this,
                    "Transformação feita com sucesso... a abrir browser...",
                    "XSLT para HTML", JOptionPane.INFORMATION_MESSAGE);
            String url = "transformaImagens.html";
            File htmlFile = new File(url);
            try {
                Desktop.getDesktop().browse(htmlFile.toURI());
            } catch (IOException ex) {
                Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jMenuItemHTMLFotosActionPerformed

